all_primes = [2]

def is_prime(n):
    for i in all_primes:
        if n % i == 0:
            return(False)
    return(True)

from time import sleep

prime = 3
try_this = 3
for i in range(int(1E4 - 1)):
    all_primes += [prime]
    print(i, " ", all_primes[-1])
    while True:
        try_this += 2
        if is_prime(try_this):
            prime = try_this
            break

print("Problem 7:", prime)

